
// Logical Operators: true/false

// AND(&&), OR(||), NOT(!)


// AND(&&), always expects all the conditions should returned true.

var a = 10
var b = 20
var c = 30

// And example
console.log(a>b && b>c)


// OR(||), always expects at least one conditions should return true.

// Or example
console.log(a<b || b<c)


// NOT(!), negation - if condition is true, it will return false, otherwise true.
var isDone=false;
console.log(!isDone)
